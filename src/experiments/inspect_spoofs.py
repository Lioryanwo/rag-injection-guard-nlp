import json
import random
from pathlib import Path
from collections import defaultdict

"""
=============================================================================
Purpose:
A debugging and inspection tool to visually compare legitimate queries, 
their true gold-standard documents, and the synthetic spoof documents 
generated by the attacker. This helps evaluate the semantic quality and 
subtlety of the attacks.
=============================================================================
"""

def read_jsonl(path: Path):
    rows = []
    if path.exists():
        with path.open("r", encoding="utf-8") as f:
            for line in f:
                rows.append(json.loads(line))
    else:
        print(f"[WARNING] File not found: {path}")

    return rows

def main():
    # Path setup 
    processed_dir = Path("data/processed_old")
    queries_path = processed_dir / "val_queries.jsonl"
    qrels_path = processed_dir / "val_qrels.json"
    corpus_path = processed_dir / "corpus_chunks.jsonl"
    spoofs_path = processed_dir / "spoof_chunks.jsonl"
    
    # Loading queries
    queries = {q["query_id"]: q for q in read_jsonl(queries_path)}
    
    # Loading qrels
    if qrels_path.exists():
        with qrels_path.open("r", encoding="utf-8") as f:
            qrels = json.load(f)
    else:
        qrels = {}

    # Loading true corpus chunks and organizing by doc_id for easy lookup
    true_chunks = defaultdict(list)
    for chunk in read_jsonl(corpus_path):
        true_chunks[chunk["doc_id"]].append(chunk)

    # Loading spoof chunks and organizing by query_id for easy lookup
    spoofs_by_query = defaultdict(list)
    spoofs = read_jsonl(spoofs_path)

    for spoof in spoofs:
        spoofs_by_query[spoof["spoof_for_query"]].append(spoof)

    queries_with_spoofs = list(spoofs_by_query.keys())
    if not queries_with_spoofs:
        print("No spoofs found in spoof_chunks.jsonl!")
        return

    N = 3 
    sample_qids = random.sample(queries_with_spoofs, min(N, len(queries_with_spoofs)))

    for i, qid in enumerate(sample_qids, 1):
        q_data = queries[qid]
        question = q_data["question"]
        answers = q_data.get("answers", ["Unknown"])
        
        print("\n" + "="*80)
        print(f"[{i}/{N}] QUERY INSPECTION: {qid}")
        print("="*80)
        print(f"QUESTION:  {question}")
        print(f"ANSWER:    {answers[0] if answers else 'None'}")
        print("-" * 80)
        
        # Printing true documents (gold standard)
        print("🟢 TRUE DOCUMENTS (Gold Standard):")
        relevant_doc_ids = qrels.get(qid, [])
        found_true = False
        
        for doc_id in relevant_doc_ids:
            chunks = true_chunks.get(doc_id, [])
            for chunk in chunks:
                if answers and answers[0].lower() in chunk["text"].lower():
                    print(f"   [Chunk ID: {chunk['chunk_id']}]")
                    print(f"   {chunk['text']}")
                    print("   " + "."*40)
                    found_true = True
        
        if not found_true:
            print("   [True document not found or answer split across chunks]")

        print("-" * 80)
        
        # Printing spoof documents
        print("🔴 SPOOF DOCUMENTS (Attacker Generated):")
        spoofs = spoofs_by_query[qid]
        for idx, spoof in enumerate(spoofs, 1):
            attack_type = spoof.get("attack_type", "Unknown")
            attack_family = spoof.get("attack_family", "Unknown")
            similarity = spoof.get("embedding_similarity_to_query", 0.0)
            
            print(f"   --- Spoof #{idx} | Family: {attack_family} | Style: {attack_type} | Similarity: {similarity:.3f} ---")
            print(f"   {spoof['text']}\n")

if __name__ == "__main__":
    main()